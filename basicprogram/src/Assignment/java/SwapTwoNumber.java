package Assignment.java;

public class SwapTwoNumber {
	public static void main(String[] args) {
		int x,y,z;
		x=1;
		y=2;
		z=3;
		System.out.println("print the value x= "+x);
		System.out.println("print the value y= "+y);
		System.out.println("print the value z= "+z);
		/*swaping*/
		x=y;
		y=z;
		z=x;
		System.out.println("print the swap value x= "+x);
		System.out.println("print the swap value y= "+y);
		System.out.println("print the swap value z= "+z);
	}

}
