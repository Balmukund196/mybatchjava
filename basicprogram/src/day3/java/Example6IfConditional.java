package day3.java;

public class Example6IfConditional {
	public static void main(String[] args) {
		int number = -5;
		if (number > 0) {
			System.out.println("POSITIVE number");
		} else if (number < 0) {
			System.out.println("NEGATIVE number");
		} else {
			System.out.println("ZERO");
		}
	}

}