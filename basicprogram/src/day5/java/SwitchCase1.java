package day5.java;

public class SwitchCase1 {
public static void main(String[] args) {
	int value=30;
	switch(value) {
	case 3:
		System.out.println("congrutulation here is the case 3 excuted");
		break;
	case 4:
		System.out.println("congrutulation here is the case 4 excuted");
		break;
	case 5:
		System.out.println("congrutulation here is the case 5 excuted");
		break;
		default:
			System.out.println("sorry none of your cases matched");
			break;
	}
}
}
