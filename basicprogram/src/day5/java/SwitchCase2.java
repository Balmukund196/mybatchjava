package day5.java;

public class SwitchCase2 {
	public static void main(String[] args) {
		int month =5;
		switch (month) {
		case 1:
			System.out.println("month is jan");
			break;
		case 2:
		System.out.println("month is feb");
		break;
		case 3:
		System.out.println("month is may");
		break;
		default:
			System.out.println("given value is unvalid or not matched in the any case");
			break;
			
		}
	}

}
