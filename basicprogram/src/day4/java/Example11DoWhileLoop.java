package day4.java;

public class Example11DoWhileLoop {
	public static void main(String[] args) {
		int n = 5, times = 10;
		do {
			System.out.println("Java do while loops:" + n);
			n++;
		} while (n <= times);
		
		n=5;
		while(n<=times) {
			System.out.println("Java while loops:" + n);
			n++;
		}
	}
	}


